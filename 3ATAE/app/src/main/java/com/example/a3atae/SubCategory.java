package com.example.a3atae;

public class SubCategory {
    String idsubcat,nomsubcat;

    public SubCategory(String idsubcat, String nomsubcat) {
        this.idsubcat = idsubcat;
        this.nomsubcat = nomsubcat;
    }

    public String getIdsubcat() {
        return idsubcat;
    }

    public void setIdsubcat(String idsubcat) {
        this.idsubcat = idsubcat;
    }

    public String getNomsubcat() {
        return nomsubcat;
    }

    public void setNomsubcat(String nomsubcat) {
        this.nomsubcat = nomsubcat;
    }
}
